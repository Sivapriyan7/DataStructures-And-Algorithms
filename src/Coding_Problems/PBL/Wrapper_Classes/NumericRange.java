package Coding_Problems.PBL.Wrapper_Classes;

public class NumericRange {

    public static void main(String[] args) {
        // Print minimum and maximum values for Byte
        System.out.println("Byte range:");
        System.out.println("min: " + Byte.MIN_VALUE);
        System.out.println("max: " + Byte.MAX_VALUE);

        // Print minimum and maximum values for Short
        System.out.println("Short range:");
        System.out.println("min: " + Short.MIN_VALUE);
        System.out.println("max: " + Short.MAX_VALUE);

        // Print minimum and maximum values for Integer
        System.out.println("Integer range:");
        System.out.println("min: " + Integer.MIN_VALUE);
        System.out.println("max: " + Integer.MAX_VALUE);

        // Print minimum and maximum values for Long
        System.out.println("Long range:");
        System.out.println("min: " + Long.MIN_VALUE);
        System.out.println("max: " + Long.MAX_VALUE);

        // Print minimum and maximum values for Float
        System.out.println("Float range:");
        System.out.println("min: " + Float.MIN_VALUE);
        System.out.println("max: " + Float.MAX_VALUE);

        // Print minimum and maximum values for Double
        System.out.println("Double range:");
        System.out.println("min: " + Double.MIN_VALUE);
        System.out.println("max: " + Double.MAX_VALUE);
    }
}
